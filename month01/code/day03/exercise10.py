"""
    while循环练习, 在终端显示数字
"""
# 显示0 1 2 3
i = 0
while i < 4:
    print(i)
    i += 1

# 显示2 3 4 5 6
i = 2
while i < 7:
    print(i)
    i += 1

# 显示1 3 5 7
i = 1
while i < 9:
    print(i)
    i += 2

# 显示8 7 6 5 4
i = 8
while i > 3:
    print(i)
    i -= 1

# 显示-1 -2 -3 -4 -5
i = -1
while i > -6:
    print(i)
    i -= 1